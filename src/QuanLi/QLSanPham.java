    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        DBAccess acc = new DBAccess();
        acc.connect();
        if(txtIDSP.getText().equals("") || txtTensp.getText().equals("") || txtChitiet.getText().equals("") || txtGia.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "vui lòng điền đầy đủ thông tin");
        }
        else
        {
            String IDSP = txtIDSP.getText();
            String TenSP = txtTensp.getText();
            String IDLoai =  (String) cbbLoai.getSelectedItem();
            String ChiTiet = txtChitiet.getText();
            int Gia = Integer.parseInt(txtGia.getText());
            ResultSet rs = acc.Query("select IDSP from Sanpham where IDSP = '"+IDSP+"'");
            try {
                if(rs.next())
                {
                    JOptionPane.showMessageDialog(this, "Mã sản phẩm đã tồn tại !!!");
                }
                else
                {
                    acc.InsertSP(IDSP,TenSP,IDLoai,ChiTiet,Gia);
                }
            } catch (SQLException ex) {
                Logger.getLogger(QLSanPham.class.getName()).log(Level.SEVERE, null, ex);
            }
            LoadData();
        }
    }//GEN-LAST:event_btnThemActionPerformed

