    public void LoadData () 
    {
        DBAccess acc = new DBAccess();
        acc.connect();
        try { 
            rs = acc.GetData2();
            tabSanpham.removeAll();
            String [] arr = {"IDSP","Tên SP","Loại","Giá(VND)","Chi Tiết"};
            model = new DefaultTableModel(arr,0);
            while(rs.next())
            {   
                Vector vec = new Vector();
                vec.add(rs.getString("IDSP"));
                vec.add(rs.getString("TenSP"));
                vec.add(rs.getNString("TenLoai")); 
                vec.add(rs.getString("Gia"));
                vec.add(rs.getString("ChiTiet"));
                
                model.addRow(vec);
            }
            tabSanpham.setModel(model);
            rs.close();
        }
        catch(Exception ex)
        {
            Logger.getLogger(QLSanPham.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

