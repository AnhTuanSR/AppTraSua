    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        DBAccess acc = new DBAccess();
        acc.connect();
        if(txtID.getText().equals("") || txtSDT.getText().equals("") || txtUsername.getText().equals("") || txtDiachi.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "vui lòng điền đầy đủ thông tin");
        }
        else
        {
            ButtonGroup group = new ButtonGroup();
            group.add(rdbNu);
            group.add(rdbNam);
            String IDNV = txtID.getText();
            String Username = txtUsername.getText();
            String GioiTinh = "";
            if(rdbNu.isSelected() == true)
            {
                GioiTinh = rdbNu.getText();
            }
            else
            {
                GioiTinh = rdbNam.getText();
            }           
            String SDT = txtSDT.getText();
            String IDCV =  (String) cbbChucvu.getSelectedItem();
            String Diachi = txtDiachi.getText();
            ResultSet rs = acc.Query("select IDNV from NhanVien where IDNV = '"+IDNV+"'");
            try {
                if(rs.next())
                {
                    JOptionPane.showMessageDialog(this, "Mã nhân viên đã tồn tại !!!");
                }
                else
                {
                    acc.InsertNV(IDNV, Username,GioiTinh, SDT, IDCV, Diachi);
                }
            } catch (SQLException ex) {
                Logger.getLogger(QLNhanvien.class.getName()).log(Level.SEVERE, null, ex);
            }
            LoadData();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void tabNhanvienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabNhanvienMouseClicked
        // TODO add your handling code here:
        ButtonGroup group = new ButtonGroup();
        group.add(rdbNu);
        group.add(rdbNam);
        int i = tabNhanvien.getSelectedRow();
        TableModel model = tabNhanvien.getModel();
        cbbChucvu.setSelectedItem(model.getValueAt(i, 4).toString());
        txtID.setText(model.getValueAt(i, 0).toString());
        txtUsername.setText(model.getValueAt(i, 1).toString());
        txtSDT.setText(model.getValueAt(i, 3).toString());
        txtDiachi.setText(model.getValueAt(i, 5).toString());   
        if(model.getValueAt(i, 2).toString().equals("Nữ"))
        {
            rdbNu.setSelected(true);
            rdbNam.setSelected(false);
        }
        else
        {
            rdbNam.setSelected(true);
            rdbNu.setSelected(false);  
        }
        txtID.setEditable(false);
    }//GEN-LAST:event_tabNhanvienMouseClicked

