    public void LoadData () 
    {
        DBAccess acc = new DBAccess();
        acc.connect();
        try { 
            rs = acc.GetData1();
            tabNhanvien.removeAll();
            String [] arr = {"IDNV","Username","Giới tính","SDT","Chức vụ","Địa chỉ"};
            DefaultTableModel model = new DefaultTableModel(arr,0);
            while(rs.next())
            {
                Vector vec = new Vector();
                vec.add(rs.getString("IDNV"));
                vec.add(rs.getString("Username"));
                vec.add(rs.getString("GioiTinh"));
                vec.add(rs.getNString("SDT"));
                vec.add(rs.getString("Chức vụ"));
                vec.add(rs.getString("DiaChi"));
                                  
                model.addRow(vec);
            }
            tabNhanvien.setModel(model);
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(QLNhanvien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

